#import "SIAbstractTableController.h"


///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
@implementation SIAbstractTableController

@synthesize tableView = _tableView;
@synthesize tableViewStyle = _tableViewStyle;
@synthesize clearsSelectionOnViewWillAppear;


///////////////////////////////////////////////////////////////////////////////////////////////////
- (id) init {
    return [self initWithStyle:UITableViewStylePlain];
}


///////////////////////////////////////////////////////////////////////////////////////////////////
- (id)initWithStyle:(UITableViewStyle)style {
    if (self = [self initWithNibName:nil bundle:nil]) {
        _tableViewStyle = style;
    }
    return self;
}


///////////////////////////////////////////////////////////////////////////////////////////////////
- (id)initWithNibName:(NSString *)nibNameOrNil bundle:(NSBundle *)nibBundleOrNil {
    if (self = [super initWithNibName:nibNameOrNil bundle:nibBundleOrNil]) {
        _tableViewStyle = UITableViewStylePlain;
        self.clearsSelectionOnViewWillAppear = YES;
    }
    return self;
}


///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
#pragma mark - View cycle

///////////////////////////////////////////////////////////////////////////////////////////////////
- (void)loadView {
    [super loadView];
    [self.view addSubview:self.status];
}


///////////////////////////////////////////////////////////////////////////////////////////////////
- (void)viewWillAppear:(BOOL)animated {
    [super viewWillAppear:animated];
    
    [self.tableView flush];

    if (self.clearsSelectionOnViewWillAppear) {
        [self.tableView deselectRowAtIndexPath:self.tableView.indexPathForSelectedRow
                                      animated:animated];
    }
}


///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
#pragma mark - UIScrollViewDelegate

///////////////////////////////////////////////////////////////////////////////////////////////////
- (void)scrollViewDidScroll:(UIScrollView *)scrollView {
    [self.tableView.refreshView egoRefreshScrollViewDidScroll:scrollView];
}


///////////////////////////////////////////////////////////////////////////////////////////////////
- (void)scrollViewDidEndDragging:(UIScrollView *)scrollView willDecelerate:(BOOL)decelerate {
    [self.tableView.refreshView egoRefreshScrollViewDidEndDragging:scrollView];
}


///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
#pragma mark - NIOperationDelegate
#pragma mark Default actions for operations

///////////////////////////////////////////////////////////////////////////////////////////////////
- (void)operationDidStart:(NSOperation *)operation {
    if (! self.request.isLoaded) {
        [self.status showLoading:YES];
    }
}


///////////////////////////////////////////////////////////////////////////////////////////////////
- (void)operationDidFinish:(NSOperation *)operation {
    [self.tableView reloadData];    
    if (! self.request.isLoaded) {
        [self.status showLoading:NO];
    }
}


///////////////////////////////////////////////////////////////////////////////////////////////////
- (void)operationDidFail:(NSOperation *)operation withError:(NSError *)error {
    [self.status showError:YES];
}


///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
#pragma mark - EGORefreshTableHeaderDelegate

///////////////////////////////////////////////////////////////////////////////////////////////////
- (void)egoRefreshTableHeaderDidTriggerRefresh:(EGORefreshTableHeaderView *)view {
    [self.request start];
}


///////////////////////////////////////////////////////////////////////////////////////////////////
- (BOOL)egoRefreshTableHeaderDataSourceIsLoading:(EGORefreshTableHeaderView *)view {
    return [self.request isExecuting];
}


///////////////////////////////////////////////////////////////////////////////////////////////////
- (NSDate *)egoRefreshTableHeaderDataSourceLastUpdated:(EGORefreshTableHeaderView *)view {
    return [NSDate date];
}


///////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////
#pragma mark - Public properties

///////////////////////////////////////////////////////////////////////////////////////////////////
- (SIRequest *)request {
    SIRequest* request = [super request];
    UITableView* tableView = [self tableView];
    request.finishBlock = ^(NIOperation* operation) {
        [tableView.refreshView egoRefreshScrollViewDataSourceDidFinishedLoading:tableView];
    };
    return request;
}

@end
